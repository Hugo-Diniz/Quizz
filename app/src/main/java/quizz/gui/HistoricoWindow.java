    private void initTable() {
        DefaultTableModel modelo = new DefaultTableModel();
        modelo.addColumn("Pontuação");
        List<Integer> historico = service.getHistorico();
        
        for (int i = 0; historico.size() > i; i++) {
            modelo.addRow(new String[] {String.valueOf(historico.get(i))});
        }
        
        historicoTable.setModel(modelo);
        historicoTable.setDefaultEditor(Object.class, null);
    }

